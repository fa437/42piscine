#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)

{
	int	x;
	int	y;

	x = 0;
	while (x < (size / 2))
	{
		y = tab[x];
		tab[x] = tab[size - x - 1];
		tab[size - x - 1] = y;
		x++;
	}
}
